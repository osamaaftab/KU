
using Context;
namespace Common.Data {
    public sealed class UnitofWork : IUnitofWork {

        /// <summary>
        /// The DbContext
        /// </summary>
        private IDbContext dbContext;

        /// <summary>
        /// The repositories
        /// </summary>
        private Dictionary<Type, object> repositories;

        /// <summary>
        /// Initializes a new instance of the <see cref="CMSUnitOfWork" /> class.
        /// </summary>
        /// <param name="contextFactory">The context factory.</param>
        public UnitofWork(IContextFactory contextFactory) {
            this.dbContext = contextFactory.DbContext;
        }

        /// <summary>
        /// Gets the repository.
        /// </summary>
        /// <typeparam name="TEntity">The type of the entity.</typeparam>
        /// <returns></returns>
        public IRepository<TEntity> GetRepository<TEntity>()
            where TEntity : class {
            if (this.repositories == null) {
                this.repositories = new Dictionary<Type, object>();
            }

            var type = typeof(TEntity);
            if (!this.repositories.ContainsKey(type)) {
                this.repositories[type] = new Repository<TEntity>(this.dbContext);
            }

            return (IRepository<TEntity>)this.repositories[type];
        }

        /// <summary>
        /// Saves all pending changes
        /// </summary>
        /// <returns>The number of objects in an Added, Modified, or Deleted state</returns>
        public int Commit() {
            // Save changes with the default options
            return this.dbContext.SaveChanges();
        }

        /// <summary>
        /// Disposes the current object
        /// </summary>
        public void Dispose() {
            this.Dispose(true);

            // ReSharper disable once GCSuppressFinalizeForTypeWithoutDestructor
            GC.SuppressFinalize(obj: this);
        }

        /// <summary>
        /// Disposes all external resources.
        /// </summary>
        /// <param name="disposing">The dispose indicator.</param>
        private void Dispose(bool disposing) {
            if (disposing) {
                if (this.dbContext != null) {
                    this.dbContext.Dispose();
                    this.dbContext = null;
                }
            }
        }
    }
}
